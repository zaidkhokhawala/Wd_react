#include <stdio.h>

int main() {
    // Declare variables of different types
    int age = 25;               // Integer
    const float PI = 3.1416;    // Constant float
    char grade = 'A';           // Character

    // Print values
    printf("Age = %d\n", age);
    printf("PI = %.4f\n", PI);
    printf("Grade = %c\n", grade);

    return 0;
}
